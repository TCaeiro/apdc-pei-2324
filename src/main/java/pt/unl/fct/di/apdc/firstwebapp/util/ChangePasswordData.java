package pt.unl.fct.di.apdc.firstwebapp.util;

public class ChangePasswordData {

	public String passAtual;
	public String passwordNova;
	public String passwordNova2;

	
	public ChangePasswordData() {
		
	}
	
	
	public ChangePasswordData(String passAtual, String passwordNova, String passwordNova2){
		this.passAtual = passAtual;
		this.passwordNova = passwordNova;
		this.passwordNova2 = passwordNova2;


}
}